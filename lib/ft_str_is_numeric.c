#include "libft.h"

int	ft_str_is_numeric(char *str)
{
	if (*str == '\0')
		return (0);
	if (*str == '-')
		str++;
	while (*str != '\0')
	{
		if (!ft_isdigit(*str))
			return (0);
	}
	return (1);
}
